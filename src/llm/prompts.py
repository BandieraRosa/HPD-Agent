"""HPD-Agent v2 提示词集合。

与 v1 的差异:

1. PLANNER_PROMPT:取消注释,把"同文件修改必须串行"作为正式规划规则。
   v1 把这块逻辑写成了注释,导致 planner 偶尔会把多个写入同一文件的子任务
   并行化,造成 apply_patch 的并发冲突。
2. SUB_TASK_PROMPT:把 v1 的行号 hunk 协议替换为 v2 的 SEARCH/REPLACE 协议。
   描述更短,因为 v2 协议本身更简单,且错误信息已经包含 hint;不再需要在
   提示词里重复每个错误码的处置建议。
3. 其它 prompt 维持原状。
"""

